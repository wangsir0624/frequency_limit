use Wangjian\FrequencyLimit\HourlyGuard;

class HourlyGuardTest extends BaseTestCase
{
    public function testPass()
    {
        $guard = new HourlyGuard($this->client, 1);
        $this->assertEquals(true, $guard->pass('test'));
    }

    public function testFail()
    {
        $guard = new HourlyGuard($this->client, 0);
        $this->assertEquals(false, $guard->pass('test'));
    }

    protected function setUp()
    {
        parent::setUp(); // TODO: Change the autogenerated stub

        $this->client->flushdb();
    }
}